#pragma once

#include <iostream>

using namespace std;

int Sum (int a, int b) {
    int result;
    result = a + b;
    return result;
}

int Divide (int a, int b) {
    int result;
    result = a / b;
    return result;
}

int Multiply (int a, int b) {
    int result;
    result = a * b;
    return result;
}
